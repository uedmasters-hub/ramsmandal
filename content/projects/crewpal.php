<section class="pj" data-reveal>
  <span class="pj__step">01 — Context</span>
  <h2 class="pj__h">A workforce that runs its day from a phone</h2>
  <p class="pj__lead">Eight thousand cabin crew manage rosters, swaps, and compliance on the move &mdash; often tired, between flights, in the few minutes they have before the next departure.</p>
  <p>The tools they relied on were fragmented and unforgiving. A scheduling mistake here doesn&rsquo;t stay small; it cascades into operational and, ultimately, safety consequences.</p>
</section>

<section class="pj" data-reveal>
  <span class="pj__step">02 — Challenge</span>
  <h2 class="pj__h">High stakes, low attention, no time to learn</h2>
  <ul class="pj__list">
    <li><strong>Distracted, short windows.</strong> The app is used in the gaps, not at a desk.</li>
    <li><strong>Errors are expensive.</strong> A wrong swap or missed rule ripples across operations.</li>
    <li><strong>No onboarding budget.</strong> Crew can&rsquo;t pause to study a manual &mdash; it has to be obvious.</li>
  </ul>
</section>

<section class="pj" data-reveal>
  <span class="pj__step">03 — Decisions</span>
  <h2 class="pj__h">Make the safe choice the easy one</h2>
  <p>I introduced <strong>real-time shift management</strong> so the roster was a single source of truth, not a guess, and built <strong>fatigue alerts</strong> that surfaced exactly where a decision was being made &mdash; not as noise, but as a nudge at the right moment.</p>
  <p>The most frequent actions &mdash; check the roster, request a swap &mdash; were stripped down to the fewest possible steps, so the common case felt instant.</p>
</section>

<div class="pj__cols" data-reveal>
  <section><h3>The tension</h3><p>Surfacing fatigue and compliance information protects people &mdash; but constant alerts get ignored.</p></section>
  <section><h3>The call</h3><p>Make the safe option the default, and alert only when it would actually change a decision.</p></section>
</div>

<section class="pj" data-reveal>
