</section>

<section class="pj" data-reveal>
  <span class="pj__step">03 — Decisions</span>
  <h2 class="pj__h">Personalised bundles, placed with intent</h2>
  <ul class="pj__list">
    <li><strong>Personalised hotel bundles</strong> tied to the booked route and dates, not a generic catalogue.</li>
    <li><strong>Honest value</strong> &mdash; the bundle&rsquo;s worth versus booking separately, shown plainly.</li>
    <li><strong>Right moment</strong> &mdash; surfaced where the traveller is already thinking about the trip, not mid-checkout.</li>
  </ul>
</section>

