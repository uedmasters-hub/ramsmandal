<section class="pj" data-reveal>
  <span class="pj__step">01 — Context</span>
  <h2 class="pj__h">Ten-plus products pulling in ten-plus directions</h2>
  <p class="pj__lead">Customer apps, enterprise tools, internal platforms &mdash; each had drifted into its own patterns. The cost showed up as slow delivery, inconsistent experience, and a design team spending its time re-solving solved problems.</p>
</section>

<section class="pj" data-reveal>
  <span class="pj__step">02 — Challenge</span>
  <h2 class="pj__h">The hard part was never the components</h2>
  <p>Anyone can build a button library. The real problem is <strong>adoption</strong>: getting independent teams, in a regulated and fast-moving organisation, to actually build on a shared system instead of around it.</p>
</section>

<section class="pj" data-reveal>
  <span class="pj__step">03 — Decisions</span>
  <h2 class="pj__h">Treat the system as a contract, and the rollout as the product</h2>
  <ul class="pj__list">
    <li><strong>Tokens and components as a shared contract</strong> &mdash; one language for colour, type, spacing, and behaviour.</li>
    <li><strong>Governance and contribution</strong> &mdash; a way for teams to extend the system without forking it.</li>
    <li><strong>Adoption as a rollout, not a launch</strong> &mdash; measured, supported, team by team.</li>
    <li><strong>Tied to delivery speed</strong> &mdash; the system earned its keep on velocity, not aesthetics.</li>
  </ul>
</section>

<div class="pj__cols" data-reveal>
  <section><h3>The tension</h3><p>Standardisation makes everything consistent &mdash; and can strip the autonomy teams need for their domain.</p></section>
  <section><h3>The call</h3><p>Opinionated defaults for the 90%, deliberate escape hatches where a product genuinely needs them.</p></section>
</div>

<section class="pj" data-reveal>
  <span class="pj__step">04 — Outcomes</span>
  <h2 class="pj__h">One system, measurably faster</h2>
  <div class="pj__metrics">
