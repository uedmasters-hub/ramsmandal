    <div class="pj__metric"><b>10+</b><span>Products aligned</span></div>
    <div class="pj__metric"><b>15</b><span>Designers, one language</span></div>
  </div>
</section>

<blockquote class="pj__quote" data-reveal>A design system is an adoption problem, not a component problem. It proves judgment, not just craft.</blockquote>

