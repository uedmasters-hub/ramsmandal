<p class="pj__note" data-reveal>An independent analysis based on the publicly available product. No proprietary data or client relationship is implied &mdash; it shows how I frame first-run friction as a set of fixable decisions.</p>

<section class="pj" data-reveal>
  <span class="pj__step">01 — Observe</span>
  <h2 class="pj__h">The first run decides everything</h2>
  <p class="pj__lead">A new user gives a delivery app about thirty seconds to prove it is worth keeping. Onboarding either gets them to a relevant first screen fast &mdash; or it asks for too much, too soon, and loses them.</p>
</section>

<section class="pj" data-reveal>
  <span class="pj__step">02 — Diagnose</span>
  <h2 class="pj__h">Asking before earning</h2>
  <ul class="pj__list">
    <li><strong>Permission walls up front.</strong> Location and notification prompts before the value is clear feel like a toll, not a benefit.</li>
    <li><strong>Empty first impression.</strong> A generic feed before personalisation kicks in wastes the most attentive moment.</li>
    <li><strong>Account friction.</strong> Forcing sign-up before any value delays the only thing the user came for.</li>
  </ul>
</section>

<section class="pj" data-reveal>
  <span class="pj__step">03 — Recommend</span>
  <h2 class="pj__h">Earn each ask</h2>
  <ul class="pj__list">
    <li><strong>Show value first</strong> &mdash; a relevant, local first screen before requesting anything.</li>
    <li><strong>Ask in context</strong> &mdash; request location at the moment it visibly improves the result.</li>
    <li><strong>Defer the account</strong> &mdash; let people browse and intend before they commit to signing up.</li>
  </ul>
</section>

<blockquote class="pj__quote" data-reveal>Every permission and every form is a withdrawal. Make a deposit of value first.</blockquote>

