<section class="pj" data-reveal>
  <span class="pj__step">01 — Observe</span>
  <h2 class="pj__h">A checkout with all the intent in the world</h2>
  <p class="pj__lead">By the time someone reaches a food-delivery checkout, they are hungry and decided. The job is simple: don&rsquo;t lose them. Yet high-intent checkouts routinely leak conversion in the last few taps.</p>
</section>

<section class="pj" data-reveal>
  <span class="pj__step">02 — Diagnose</span>
  <h2 class="pj__h">Where intent meets friction</h2>
  <ul class="pj__list">
    <li><strong>Late surprises.</strong> Fees, taxes, and minimums that appear at the end re-open a decision the user thought was made.</li>
    <li><strong>Coupon anxiety.</strong> A prominent promo field invites people to leave and hunt for a code, breaking the flow.</li>
    <li><strong>Address and timing friction.</strong> Re-confirming details that should already be known adds taps at the worst moment.</li>
    <li><strong>Upsell over clarity.</strong> Add-on prompts competing with the one button that matters.</li>
  </ul>
</section>

<section class="pj" data-reveal>
  <span class="pj__step">03 — Recommend</span>
  <h2 class="pj__h">Close the leak</h2>
  <ul class="pj__list">
    <li><strong>Show the true total early</strong> &mdash; no end-of-flow surprises to renegotiate.</li>
    <li><strong>Make savings ambient</strong> &mdash; surface the best applicable offer instead of an empty code box.</li>
    <li><strong>Default the known</strong> &mdash; address, time, and payment pre-filled and editable, not re-entered.</li>
    <li><strong>Protect the primary action</strong> &mdash; one unmistakable path to pay; upsell never competes with it.</li>
  </ul>
</section>

<blockquote class="pj__quote" data-reveal>The last three taps are where intent is won or lost. Spend your clarity budget there.</blockquote>

