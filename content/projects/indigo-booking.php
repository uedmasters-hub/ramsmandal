<section class="pj" data-reveal>
  <span class="pj__step">01 — Context</span>
  <h2 class="pj__h">The commercial engine of India&rsquo;s largest airline</h2>
  <p class="pj__lead">Every fare, every add-on, every payment for tens of millions of travellers runs through one flow. At that scale, a few seconds of confusion is not a usability nit &mdash; it is lost revenue and a flood of support calls.</p>
  <p>The booking experience had to serve a first-time flyer booking once a year and a business traveller booking weekly, on the same screens, across every device and network condition in the country.</p>
</section>

<section class="pj" data-reveal>
  <span class="pj__step">02 — Challenge</span>
  <h2 class="pj__h">Sell more without getting in the way</h2>
  <p>Airlines live on ancillaries &mdash; seats, bags, meals, insurance. The pressure is always to add more upsell surface. But every extra prompt taxes the one thing that actually drives the business: a clear, fast path to <strong>pay</strong>.</p>
  <ul class="pj__list">
    <li><strong>Two audiences, one flow.</strong> Occasional and frequent flyers needed different speeds without two separate products.</li>
    <li><strong>Regulated clarity.</strong> Fare and fee display rules left little room for ambiguity.</li>
    <li><strong>Spiky load.</strong> Sale events like Super 6E turned the flow into a stress test overnight.</li>
    <li><strong>Real-world devices.</strong> Low-end phones and patchy networks were the norm, not the edge case.</li>
  </ul>
</section>

<section class="pj" data-reveal>
  <span class="pj__step">03 — Decisions</span>
  <h2 class="pj__h">Make the cleanest path also the most profitable one</h2>
  <p>I treated the flow as a sequence with one spine &mdash; fare, then add-ons, then pay &mdash; and made every ancillary <strong>contextual</strong>: the right offer at the step where it actually makes sense, rather than a wall of prompts up front.</p>
  <p>At payment, the work was subtraction: fewer decisions, clearer totals, fewer reasons to abandon. For Super 6E Sale we designed explicitly for the spike, so the highest-traffic moment of the year was also the calmest.</p>
</section>

<div class="pj__cols" data-reveal>
  <section><h3>The tension</h3><p>More upsell surface promises more revenue &mdash; and quietly erodes the clarity that gets people to checkout at all.</p></section>
  <section><h3>The call</h3><p>Earn the upsell with relevance, never volume. Protect the path to &ldquo;pay&rdquo; as the one thing that must stay frictionless.</p></section>
</div>

